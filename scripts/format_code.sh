#!/bin/bash

# Скрипт для форматирования кода Flutter проекта

echo "🔧 Запуск форматирования кода..."

# Переход в корневую директорию проекта
cd "$(dirname "$0")/.." || exit

# Форматирование всех Dart файлов в папке lib
echo "📝 Форматирование файлов в lib/..."
dart format lib/ --line-length 100

echo "✅ Форматирование завершено!"
